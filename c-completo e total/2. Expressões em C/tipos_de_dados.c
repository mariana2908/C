// Há cinco tipos de dados em C: caractere, inteiro, float, double e void.

//  TODOS OS TIPOS DE DADOS DEFINIDOS NO PADRÃO ANSI
// caractere
char a;
unsigned char b;
signed char c;

// inteiro
int d;
unsigned int e;
signed int f;
short int g;
unsigned short int h;
signed short int i;
long int j;
signed long int k;
unsigned long int l;

// real
float m;
double n;
long double o;

//  MODIFICANDO OS TIPOS BÁSICOS
signed; // adiciona sinal (positivo ou negativo) (inútil para int, ele será automaticamente do tipo signed)
unsigned; // sinal apenas positivo (incluindo o 0)
long; // aumenta o número de bytes usados para o registro do número, o que resulta em um intervalo de validade maior significativamente
short; // especifica uma variável com tamanho menor que o tipo base